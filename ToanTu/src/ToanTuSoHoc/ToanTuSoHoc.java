package ToanTuSoHoc;

public class ToanTuSoHoc {

	public static void main(String[] args) {
		int a = 13;
		int b = 21;
		
		int sum = a + b;
		int hieu = a - b;
		int tich = a * b;
		int thuong = a / b;
		int du = a % b;
		
		
		
		System.out.println("a tang len 1" + a++); 
		System.out.println("a giam di 1" + a--);
		System.out.println("tong a va b la: " + sum);
		System.out.println("hieu a va b la: " + hieu);
		System.out.println("tich a va b la: " + tich);
		System.out.println("thuong a va b la: " + thuong);
		System.out.println("du a va b la: " + du);
	}

}
